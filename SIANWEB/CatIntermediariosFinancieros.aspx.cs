        private void Guardar()
        {
            try
            {
                Sesion session = new Sesion();
                session = (Sesion)Session["Sesion" + Session.SessionID];

                IntermediarioFinanciero inter = new IntermediarioFinanciero();
                inter.Id_IF = Convert.ToInt32(txtClave.Text);
                inter.Banco = txtBanco.Text;
                inter.RazonSocial = txtRazonSocial.Text;
                inter.Activo = chkActivo.Checked.ToString();
                inter.Correo = txtCorreo.Text;
                inter.Id_Emp = session.Id_Emp;
                inter.Id_Cd = session.Id_Cd_Ver;
                inter.RFC = txtRfc.Text;

                CN_IntermediarioFinanciero intermediario = new CN_IntermediarioFinanciero();
                int verificador = -1;

                if (HF_ID.Value == "")
                {
                    if (!_PermisoGuardar)
                    {
                        Alerta("No tiene permisos para grabar");
                        return;
                    }
                    intermediario.InsertarIntermediarioFinanciero(inter, session.Emp_Cnx, ref verificador);
                    if (verificador == 1)
                    {
                        Nuevo();
                        Alerta("Los datos se guardaron correctamente");
                        txtClave.Text = Valor;
                    }
                    else
                    {
                        Alerta("La clave ya existe");
                    }
                }
                else
                {
                    if (!_PermisoModificar)
                    {
                        Alerta("No tiene permisos para modificar");
                        return;
                    }                     
                    intermediario.ModificarIntermediarioFinanciero(inter, session.Emp_Cnx, ref verificador);
                    if (verificador == 1)
                    {
                        Nuevo();
                        Alerta("Los datos se modificaron correctamente");
                        txtClave.Text = Valor;
                    }
                    else
                    {
                        Alerta("La clave ya existe");
                    }
                }
                rgBancos.Rebind();
            }
            catch (Exception ex)
            {
                throw ex;
            }
        }
  
